// Track if user has scrolled to the bottom of contract
let hasReadContract = false;

// Set today's date as default
document.addEventListener('DOMContentLoaded', function() {
    const today = new Date().toISOString().split('T')[0];
    document.getElementById('acceptanceDate').value = today;
    
    // Hide form initially
    const form = document.getElementById('rider-contract-form');
    form.classList.remove('visible');
    
    // Detect browser language and set accordingly
    const browserLang = navigator.language || navigator.userLanguage;
    const langCode = browserLang.split('-')[0];
    const languageSelect = document.getElementById('language');
    
    // Always default to English first
    languageSelect.value = 'en';
    
    // Load English content immediately
    changeLanguage();
    
    // Force load content if still empty after changeLanguage
    setTimeout(function() {
        const contractContentDiv = document.getElementById('contract-content');
        if (!contractContentDiv.innerHTML.trim()) {
            const enContent = contractContent['en'];
            if (enContent && enContent.content) {
                contractContentDiv.innerHTML = enContent.content;
                document.getElementById('page-title').textContent = enContent.pageTitle;
                document.getElementById('header-subtitle').textContent = enContent.headerSubtitle;
            }
        }
    }, 100);
    
    // Then check if browser language is supported and switch
    if (contractContent[langCode] && langCode !== 'en') {
        languageSelect.value = langCode;
        changeLanguage();
    }
    
    // Auto-convert PAN to uppercase as user types
    const panInput = document.getElementById('panNumber');
    if (panInput) {
        panInput.addEventListener('input', function() {
            this.value = this.value.toUpperCase();
        });
    }
    
    // Copy work location to signed location
    const workLocationInput = document.getElementById('workLocation');
    const signedLocationInput = document.getElementById('signedLocation');
    
    if (workLocationInput && signedLocationInput) {
        // Set initial value if work location has value
        if (workLocationInput.value) {
            signedLocationInput.value = workLocationInput.value;
        }
        
        // Update signed location when work location changes
        workLocationInput.addEventListener('input', function() {
            signedLocationInput.value = this.value;
        });
    }
    
    // Initially disable the form
    disableForm();
    
    // Listen for checkbox change
    const confirmCheckbox = document.getElementById('confirmReadTerms');
    confirmCheckbox.addEventListener('change', function() {
        if (this.checked) {
            hasReadContract = true;
            enableForm();
        } else {
            hasReadContract = false;
            disableForm();
        }
    });
    
    // Handle vehicle type change for bicycle
    const vehicleTypeSelect = document.getElementById('vehicleType');
    const vehicleNumberInput = document.getElementById('vehicleNumber');
    const vehicleNumberGroup = document.getElementById('vehicleNumberGroup');
    const licenseNumberInput = document.getElementById('licenseNumber');
    const licenseNumberGroup = document.getElementById('licenseNumberGroup');
    const hasLicenseCheckbox = document.getElementById('hasLicense');
    const hasVehicleDocsCheckbox = document.getElementById('hasVehicleDocs');
    
    // Auto-convert Vehicle Number to uppercase as user types
    if (vehicleNumberInput) {
        vehicleNumberInput.addEventListener('input', function() {
            this.value = this.value.toUpperCase();
        });
    }
    
    // Function to handle vehicle type change
    function handleVehicleTypeChange() {
        if (vehicleTypeSelect.value === 'bike') { // bicycle
            // Hide vehicle number and license number fields
            vehicleNumberGroup.style.display = 'none';
            licenseNumberGroup.style.display = 'none';
            
            // Set default values for hidden fields
            vehicleNumberInput.value = 'Bi-cycle';
            vehicleNumberInput.required = false;
            licenseNumberInput.value = 'Not Required';
            licenseNumberInput.required = false;
            
            // Make checkboxes not required
            hasLicenseCheckbox.required = false;
            hasVehicleDocsCheckbox.required = false;
        } else {
            // Show vehicle number and license number fields
            vehicleNumberGroup.style.display = 'block';
            licenseNumberGroup.style.display = 'block';
            
            // Clear values and make required
            vehicleNumberInput.value = '';
            vehicleNumberInput.required = true;
            licenseNumberInput.value = '';
            licenseNumberInput.required = true;
            
            // Make checkboxes required
            hasLicenseCheckbox.required = true;
            hasVehicleDocsCheckbox.required = true;
        }
    }
    
    vehicleTypeSelect.addEventListener('change', handleVehicleTypeChange);
    
    // Set initial state on page load
    handleVehicleTypeChange();
    
    // Validate checkboxes and enable/disable submit button
    const submitButton = document.querySelector('#rider-contract-form button[type="submit"]');
    const agreeTermsCheckbox = document.getElementById('agreeTerms');
    const ownDocumentsCheckbox = document.getElementById('ownDocuments');
    
    function validateCheckboxes() {
        const vehicleType = vehicleTypeSelect.value;
        let allChecked = true;
        let missingCheckboxes = [];
        
        // Always required: agreeTerms and ownDocuments
        if (!agreeTermsCheckbox.checked) {
            allChecked = false;
            missingCheckboxes.push('I agree to all terms and conditions');
            agreeTermsCheckbox.parentElement.style.border = '2px solid red';
            agreeTermsCheckbox.parentElement.style.padding = '10px';
            agreeTermsCheckbox.parentElement.style.borderRadius = '5px';
            agreeTermsCheckbox.parentElement.style.background = '#ffe6e6';
        } else {
            agreeTermsCheckbox.parentElement.style.border = '';
            agreeTermsCheckbox.parentElement.style.padding = '';
            agreeTermsCheckbox.parentElement.style.background = '';
        }
        
        if (!ownDocumentsCheckbox.checked) {
            allChecked = false;
            missingCheckboxes.push('I own original Aadhar and PAN documents');
            ownDocumentsCheckbox.parentElement.style.border = '2px solid red';
            ownDocumentsCheckbox.parentElement.style.padding = '10px';
            ownDocumentsCheckbox.parentElement.style.borderRadius = '5px';
            ownDocumentsCheckbox.parentElement.style.background = '#ffe6e6';
        } else {
            ownDocumentsCheckbox.parentElement.style.border = '';
            ownDocumentsCheckbox.parentElement.style.padding = '';
            ownDocumentsCheckbox.parentElement.style.background = '';
        }
        
        // Only required for non-bicycle vehicles
        if (vehicleType !== 'bike') {
            if (!hasLicenseCheckbox.checked) {
                allChecked = false;
                missingCheckboxes.push('I have a valid driving license');
                hasLicenseCheckbox.parentElement.style.border = '2px solid red';
                hasLicenseCheckbox.parentElement.style.padding = '10px';
                hasLicenseCheckbox.parentElement.style.borderRadius = '5px';
                hasLicenseCheckbox.parentElement.style.background = '#ffe6e6';
            } else {
                hasLicenseCheckbox.parentElement.style.border = '';
                hasLicenseCheckbox.parentElement.style.padding = '';
                hasLicenseCheckbox.parentElement.style.background = '';
            }
            
            if (!hasVehicleDocsCheckbox.checked) {
                allChecked = false;
                missingCheckboxes.push('I have valid vehicle registration documents');
                hasVehicleDocsCheckbox.parentElement.style.border = '2px solid red';
                hasVehicleDocsCheckbox.parentElement.style.padding = '10px';
                hasVehicleDocsCheckbox.parentElement.style.borderRadius = '5px';
                hasVehicleDocsCheckbox.parentElement.style.background = '#ffe6e6';
            } else {
                hasVehicleDocsCheckbox.parentElement.style.border = '';
                hasVehicleDocsCheckbox.parentElement.style.padding = '';
                hasVehicleDocsCheckbox.parentElement.style.background = '';
            }
        }
        
        // Enable or disable submit button
        if (allChecked) {
            submitButton.disabled = false;
            submitButton.style.opacity = '1';
            submitButton.style.cursor = 'pointer';
            submitButton.title = 'Click to submit your contract';
        } else {
            submitButton.disabled = true;
            submitButton.style.opacity = '0.5';
            submitButton.style.cursor = 'not-allowed';
            submitButton.title = 'Please check all required checkboxes: ' + missingCheckboxes.join(', ');
        }
    }
    
    // Add event listeners to all checkboxes
    agreeTermsCheckbox.addEventListener('change', validateCheckboxes);
    ownDocumentsCheckbox.addEventListener('change', validateCheckboxes);
    hasLicenseCheckbox.addEventListener('change', validateCheckboxes);
    hasVehicleDocsCheckbox.addEventListener('change', validateCheckboxes);
    vehicleTypeSelect.addEventListener('change', validateCheckboxes);
    
    // Initial validation
    validateCheckboxes();
});

function disableForm() {
    const form = document.getElementById('rider-contract-form');
    const inputs = form.querySelectorAll('input, select, textarea, button');
    
    inputs.forEach(input => {
        input.disabled = true;
    });
    
    // Show notice
    const notice = document.createElement('div');
    notice.id = 'read-contract-notice';
    notice.className = 'read-contract-notice';
    notice.innerHTML = '<strong>⚠️ Please read the full contract by scrolling to the bottom to enable this form.</strong>';
    form.insertBefore(notice, form.firstChild);
}

function enableForm() {
    const form = document.getElementById('rider-contract-form');
    const inputs = form.querySelectorAll('input, select, textarea, button');
    
    // Show the form with animation
    form.classList.add('visible');
    
    inputs.forEach(input => {
        input.disabled = false;
    });
    
    // Remove notice
    const notice = document.getElementById('read-contract-notice');
    if (notice) {
        notice.remove();
    }
    
    // Show success message
    const successNotice = document.createElement('div');
    successNotice.className = 'read-contract-success';
    successNotice.innerHTML = '<strong>✓ Thank you for reading the contract. You may now fill the form below.</strong>';
    form.insertBefore(successNotice, form.firstChild);
    
    // Scroll to form smoothly
    setTimeout(() => {
        form.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }, 300);
    
    // Remove success message after 5 seconds
    setTimeout(() => {
        successNotice.remove();
    }, 5000);
}

